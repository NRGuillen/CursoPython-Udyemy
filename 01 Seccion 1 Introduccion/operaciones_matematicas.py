a = 10
b = 3.5

add = a + b
restar = a - b
multiplicar = a * b
division = a / b

division_integer = a // b # Sin decimales
resto = a % b
potencia = a ** 2 # 100


print(f'Suma: {add}')
print(f'Resta: {restar}')
print(f'Multiplicacion: {multiplicar}')
print(f'Division: {division}')
print(f'Division sin decimales: {division_integer}')
print(f'Resto de division: {resto}')
print(f'Potencia de un numero (10 ^2 ): {potencia}')

